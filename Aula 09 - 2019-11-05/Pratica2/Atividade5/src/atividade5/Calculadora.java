package atividade5;

public class Calculadora {
    
    static int soma(int x, int y) {
        return x + y;
    }

    static int sub(int x, int y) {
        return x - y;
    }

    static int mult(int x, int y) {
        return x * y;
    }

    static float div(int x, int y) {
        return x / y;
    }
}
