package atividade1;

public class Calculadora {

    float x;
    float y;
    
    void soma() {
        System.out.println("Soma\t= " + (x + y));
    }
    
    void sub() {
        System.out.println("Sub\t= " + (x - y));
    }
    
    void mult() {
        System.out.println("Mult\t= " + (x * y));
    }
    
    void div() {
        System.out.println("Div\t= " + (x / y));
    }
}
