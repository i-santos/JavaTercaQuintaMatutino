package aula8_atividade2;

public class Calculadora {
    
    void soma(int n1, int n2) {
        System.out.println("A soma é: " + (n1 + n2));
    }
    
    void subtracao(int n1, int n2) {
        System.out.println("A subtraçãp é: " + (n1 - n2));
    }
    
    void multiplicacao(int n1, int n2) {
        System.out.println("A multiplicação é: " + (n1 * n2));
    }
    
    void divisao(int n1, int n2) {
        System.out.println("A divisão é: " + (n1 / n2));
    }
}
