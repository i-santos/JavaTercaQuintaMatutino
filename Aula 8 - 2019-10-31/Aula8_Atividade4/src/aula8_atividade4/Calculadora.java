public class Calculadora {
    
    int soma(int n1, int n2) {
        return n1 + n2;
    }
    
    int subtracao(int n1, int n2) {
        return n1 - n2;
    }
    
    int multiplicacao(int n1, int n2) {
        return n1 * n2;
    }
    
    int divisao(int n1, int n2) {
        return n1 / n2;
    }
}
